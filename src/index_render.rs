fn routes_page(doc: &IndexDoc) -> String {
    let mut out = page("SIM Index Routes");
    if doc.routes.is_empty() {
        out.push_str("No route rows are present.\n");
        return out;
    }
    out.push_str("| Route | Title | Steps |\n| --- | --- | --- |\n");
    for route in &doc.routes {
        out.push_str(&format!(
            "| `{}` | {} | {} |\n",
            route.id,
            cell(&route.title),
            route
                .steps
                .iter()
                .map(route_step_label)
                .collect::<Vec<_>>()
                .join(", ")
        ));
    }
    out
}

fn page(title: &str) -> String {
    format!("# {title}\n\n{GENERATED}\n\n")
}

fn package_subjects(doc: &IndexDoc) -> impl Iterator<Item = &SubjectRecord> {
    doc.subjects.iter().filter(|subject| {
        matches!(
            subject.kind.as_str(),
            "repo" | "crate" | "package" | "doc-set"
        )
    })
}

fn contains_count(doc: &IndexDoc, subject: &str) -> usize {
    doc.edges
        .iter()
        .filter(|edge| edge.rel == "contains" && edge.from == subject)
        .count()
}

fn looks_framework(value: &str) -> bool {
    let value = value.to_ascii_lowercase();
    value.contains("framework") || value.contains("runtime-lib") || value.contains("library")
}

fn looks_language(value: &str) -> bool {
    let value = value.to_ascii_lowercase();
    value.contains("language")
        || value.contains("grammar")
        || value.contains("codec")
        || value.contains("syntax")
}

fn route_step_label(step: &RouteStep) -> String {
    match step {
        RouteStep::Feature(id) => format!("`{id}`"),
        RouteStep::Specimen(id) => format!("`{id}`"),
    }
}

fn card_count(doc: &IndexDoc) -> usize {
    doc.subjects.len() + doc.features.len() + doc.specimens.len() + doc.routes.len()
}

fn cards_jsonl(doc: &IndexDoc) -> Result<String, String> {
    let mut rows = Vec::new();
    for subject in &doc.subjects {
        rows.push(json!({
            "kind": "subject",
            "id": subject.id.as_str(),
            "subject_kind": subject.kind,
            "title": subject.title,
        }));
    }
    for feature in &doc.features {
        rows.push(feature_card(feature));
    }
    for specimen in &doc.specimens {
        rows.push(specimen_card(specimen));
    }
    for route in &doc.routes {
        rows.push(route_card(route));
    }
    rows.sort_by(|left, right| {
        (
            left["kind"].as_str().unwrap_or_default(),
            left["id"].as_str().unwrap_or_default(),
        )
            .cmp(&(
                right["kind"].as_str().unwrap_or_default(),
                right["id"].as_str().unwrap_or_default(),
            ))
    });
    let mut out = String::new();
    for row in rows {
        out.push_str(&serde_json::to_string(&row).map_err(|err| format!("serialize card: {err}"))?);
        out.push('\n');
    }
    Ok(out)
}

fn feature_card(feature: &FeatureRecord) -> Value {
    json!({
        "kind": "feature",
        "id": feature.id.as_str(),
        "canonical_key": feature.key.as_str(),
        "subject": feature.subject.as_str(),
        "title": feature.title,
        "summary": feature.summary,
        "anchors": feature.anchors.iter().map(ToString::to_string).collect::<Vec<_>>(),
        "surfaces": feature.surfaces.iter().map(ToString::to_string).collect::<Vec<_>>(),
        "specimens": feature.specimens.iter().map(ToString::to_string).collect::<Vec<_>>(),
    })
}

fn specimen_card(specimen: &DiscoveredSpecimen) -> Value {
    json!({
        "kind": "specimen",
        "id": specimen.id.as_str(),
        "subject": specimen.subject.as_str(),
        "specimen_kind": specimen.kind,
        "path": specimen.path,
        "language": specimen.language,
        "runnable": specimen.runnable,
        "checked": specimen.checked,
        "checked_by": specimen.checked_by,
    })
}

fn route_card(route: &RouteRecord) -> Value {
    json!({
        "kind": "route",
        "id": route.id.as_str(),
        "title": route.title,
        "steps": route.steps.iter().map(route_step_id).collect::<Vec<_>>(),
    })
}

fn route_step_id(step: &RouteStep) -> String {
    match step {
        RouteStep::Feature(id) => id.to_string(),
        RouteStep::Specimen(id) => id.to_string(),
    }
}

fn find_rows(doc: &IndexDoc, query: &str) -> Vec<Value> {
    let needle = query.to_ascii_lowercase();
    let mut rows = Vec::new();
    for subject in &doc.subjects {
        if matches_query(
            &needle,
            &[subject.id.as_str(), &subject.kind, &subject.title],
        ) {
            rows.push(json!({
                "kind": "subject",
                "id": subject.id.as_str(),
                "title": subject.title,
                "summary": subject.kind,
            }));
        }
    }
    for feature in &doc.features {
        if matches_query(
            &needle,
            &[
                feature.id.as_str(),
                feature.key.as_str(),
                feature.subject.as_str(),
                &feature.title,
                &feature.summary,
            ],
        ) {
            rows.push(json!({
                "kind": "feature",
                "id": feature.id.as_str(),
                "title": feature.title,
                "summary": feature.summary,
            }));
        }
    }
    for specimen in &doc.specimens {
        if matches_query(
            &needle,
            &[
                specimen.id.as_str(),
                specimen.subject.as_str(),
                &specimen.kind,
                &specimen.path,
            ],
        ) {
            rows.push(json!({
                "kind": "specimen",
                "id": specimen.id.as_str(),
                "title": specimen.path,
                "summary": specimen.kind,
            }));
        }
    }
    for route in &doc.routes {
        if matches_query(&needle, &[route.id.as_str(), &route.title]) {
            rows.push(json!({
                "kind": "route",
                "id": route.id.as_str(),
                "title": route.title,
                "summary": route.steps.len().to_string(),
            }));
        }
    }
    rows.sort_by(|left, right| {
        (
            left["kind"].as_str().unwrap_or_default(),
            left["id"].as_str().unwrap_or_default(),
        )
            .cmp(&(
                right["kind"].as_str().unwrap_or_default(),
                right["id"].as_str().unwrap_or_default(),
            ))
    });
    rows
}

fn matches_query(needle: &str, fields: &[&str]) -> bool {
    fields
        .iter()
        .any(|field| field.to_ascii_lowercase().contains(needle))
}

fn cell(value: &str) -> String {
    value.replace(['\n', '\r'], " ").replace('|', "\\|")
}

fn with_newline(mut value: String) -> String {
    if !value.ends_with('\n') {
        value.push('\n');
    }
    value
}

